    private void cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cariActionPerformed
        // TODO add your handling code here:
    String tampilan = "yyyy-MM-dd";
    SimpleDateFormat fm = new SimpleDateFormat (tampilan);
    String tanggal=String.valueOf(fm.format(tgl.getDate()));       
    try
        {
            Statement statement = (Statement) koneksi.getKoneksi().createStatement();
	    ResultSet rs = statement.executeQuery ("SELECT * FROM transaksi WHERE tanggal like '%"+tanggal+"%'");
            DefaultTableModel dtm = (DefaultTableModel) TabTiket.getModel();

            dtm.setRowCount(0);
            String [] data = new String[10];
            int i = 1;

            while(rs.next())
            {
                data[0] = rs.getString("kd_tiket");
                data[1] = rs.getString("pegawai");
                data[2] = rs.getString("tanggal");
                data[3] = rs.getString("qty_dewasa");
                data[4] = rs.getString("hrg_tiket_dewasa");
                data[5] = rs.getString("qty_anak");
                data[6] = rs.getString("hrg_tiket_anak");
                data[7] = rs.getString("disc member");
                data[8] = rs.getString("disc promo");
                data[9]= rs.getString("total");
            
                dtm.addRow(data);
                i++;
            }
           rs.close();
            tgl.setDate(null);
            a.setText(Integer.toString(getSum()));
        
    }
        catch(Exception ex)  {
            JOptionPane.showMessageDialog(null,"Data yang Anda cari Tidak dapat Ditemukan");
            System.err.println("error (search) : " +ex);
        }
    
    }//GEN-LAST:event_cariActionPerformed

    private void printActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_printActionPerformed
        // TODO add your handling code here:
        try{
            HashMap param = new HashMap(1);
            InputStream url=getClass().getResourceAsStream("/iReport/rpttrans.jrxml");
            JasperDesign jdes=JRXmlLoader.load(url);
            java.util.Locale lok= new Locale ("id","ID");
            param.put("tanggal1",(tgl.getDate()));
            param.put(JRParameter.REPORT_LOCALE, lok);
            JasperReport jrp = JasperCompileManager.compileReport(jdes);
            JasperPrint jprint = JasperFillManager.fillReport(jrp, param, koneksi.getKoneksi());
            JasperViewer.viewReport(jprint, false);
            
        } catch (JRException e) {
            JOptionPane.showMessageDialog(null, e);  
        }
    }//GEN-LAST:event_printActionPerformed

