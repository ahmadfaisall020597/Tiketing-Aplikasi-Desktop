    private int getSum() {
        int jumlah = TabTiket.getRowCount();
        int sum=0;
        for (int i = 0; i < jumlah; i++) {
        sum = sum+Integer.parseInt(TabTiket.getValueAt(i,9).toString());
        }
        return sum;
    }

