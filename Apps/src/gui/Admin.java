    private void kodeotomatis() {
        try {
            Statement stat = conn.createStatement();
            String sql ="select * from pegawai order by id desc";
            ResultSet hasil = stat.executeQuery(sql);
            if (hasil.next()) {
                String no = hasil.getString("id").substring(1);
                String AN = "" + (Integer.parseInt(no) + 1);
                String Nol = "";
                
                if(AN.length()==1)
                {Nol = "000";}
                else if(AN.length()==2)
                {Nol = "00";}
                else if(AN.length()==3)
                {Nol = "";}
                else if(AN.length()==4)
                {Nol = "";}
                
                id.setText("K" + Nol + AN);
            }else {
                id.setText("K0001");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    
