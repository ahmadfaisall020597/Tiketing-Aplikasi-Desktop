    private void ubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ubahActionPerformed
        // TODO add your handling code here:
        int ok = JOptionPane.showConfirmDialog(null,"Yakin untuk diubah ?","Konfirmasi Dialog",
                JOptionPane.YES_NO_OPTION);
        if(ok==0) {
        String sql = "Update pegawai set nama=?,"+"j_kelamin=?," +"alamat=?," +"no_telp=?"
                    +"where id='"+id.getText()+"'";
        try {
           PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1,nama.getText());
            if(lk.isSelected()) {
                jeniskelamin="Laki-laki";
            }if (pr.isSelected()) {
                jeniskelamin="Perempuan";
            }
            stat.setString(2,jeniskelamin);
            stat.setString(3,alamat.getText());
            stat.setString(4,notelp.getText());
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null,"Data Berhasil diubah");
            kosong();
            kodeotomatis();
            dataTable();
        } catch(SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
        }
    
    }//GEN-LAST:event_ubahActionPerformed

    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
        String sql ="Insert into pegawai values (?,?,?,?,?)";
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1,id.getText());
            stat.setString(2,nama.getText());
            if(lk.isSelected()) {
                jeniskelamin="Laki-laki";
            }if (pr.isSelected()) {
                jeniskelamin="Perempuan";
            }
            stat.setString(3,jeniskelamin);
            stat.setString(4,alamat.getText());
            stat.setString(5,notelp.getText());
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null,"Data Berhasil disimpan");
            kosong();
            kodeotomatis();
            dataTable();
        } catch(SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
        
    }//GEN-LAST:event_simpanActionPerformed

