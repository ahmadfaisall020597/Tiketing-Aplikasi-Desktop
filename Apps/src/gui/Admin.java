    protected void dataTable() {
        Object[] baris = {"Id Karyawan","Nama Karyawan","Jenis Kelamin","Alamat","No Telepon"};
        tabmode = new DefaultTableModel(null,baris);
        table.setModel(tabmode);
        String sql="select * from pegawai";
        try {
            Statement stat = (Statement) koneksi.getKoneksi().createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()) {
                String sid = hasil.getString("id");
                String snama = hasil.getString("nama");
                String sjkelamin = hasil.getString("j_kelamin");
                String salamat = hasil.getString("alamat");
                String snotelp = hasil.getString("no_telp");
                String[]data = {sid,snama,sjkelamin,salamat,snotelp};
                tabmode.addRow(data);
            }
        } catch (Exception e) {
        }
    }
